/*
Variables are Containers for Storing Data
JavaScript Variables can be declared in 4 ways:

Automatically
Using var
Using let
Using const
*/
// Using var
var x = 5;
var y = 6;
var z = x + y;
console.log(typeof x);


// Using let
let x1 = 5;
let y1 = 6;
let z1 = x1 + y1;
console.log(typeof x1);

// Using const
const x2 = 5;
const y2 = 6;
const z2 = x2 + y2;
console.log(typeof x2);
